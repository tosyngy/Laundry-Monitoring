 void gatData(String data){
   double advance=0;
    double bal=0;
    String date="";
    try {
        if (dt==1) {
          dbcon.rs = dbcon.st.executeQuery("select * from bill_record WHERE bill_record.date = '"+data+"'");
                   
        } else {
             dbcon.rs = dbcon.st.executeQuery("select * from bill_record");
         
        }
           while (dbcon.rs.next()) { 
                  dbcon2.rs = dbcon2.st.executeQuery("select * from update_payment WHERE bill_no = '"+dbcon.rs.getString("bill_no")+"' ORDER BY id");
                    while (dbcon2.rs.next()) {                        
                      advance+=dbcon2.rs.getDouble("amt"); 
                      bal=dbcon2.rs.getDouble("balance");
                      date=dbcon2.rs.getString("date");
                    }
                Object[] dat = new Object[]{++j,dbcon.rs.getString("bill_no"),dbcon.rs.getString("cusid"),dbcon.rs.getString("total"),advance,bal,dbcon.rs.getString("date"),date};
                dataSet.addRow(dat);
            }
                jTable1.setModel(dataSet);
    } catch (Exception e) {
        e.printStackTrace();
    }
 }
 
