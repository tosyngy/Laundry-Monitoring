   private void update(String adv,String bal) {
    SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MMM-yyyy");
        try {
            dbcon.ps = dbcon.con.prepareStatement("delete from update_payment where bill_no='"+jTextField1.getText()+"'");
            dbcon.ps.execute();
             dbcon.ps = dbcon.con.prepareStatement("insert into update_payment values (?,?,?,?,?)");
                    dbcon.ps.setInt(1, 0);
                    dbcon.ps.setString(2, jTextField1.getText());
                    dbcon.ps.setString(3, adv);
                    dbcon.ps.setString(4, bal);
                    dd=new Date();
                    dd=new Date(dd.getTime());
                    dbcon.ps.setString(5, dateFormat.format(dd));
                    dbcon.ps.executeUpdate();
                    
         } catch (Exception e) {
            System.out.println("red " + e.getMessage());
        }
        JOptionPane.showMessageDialog(rootPane, "Data Updated");
    }
